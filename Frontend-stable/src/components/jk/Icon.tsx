export function Icon({
  name,
  className = "",
  style,
  shadowed,
}: {
  name: string;
  className?: string;
  style?: React.CSSProperties;
  shadowed?: boolean;
}) {
  const LucideIconComponent = ICON_MAP[name];
  if (!LucideIconComponent) {
    return null;
  }

  const iconSize = className ? undefined : 20;

  if (shadowed) {
    return (
      <span className="relative inline-flex items-center justify-center leading-none">
        <LucideIconComponent
          className={className}
          size={iconSize}
          strokeWidth={3}
          style={{ ...style, position: "absolute", left: 0, top: 0, opacity: 0.25, transform: "translate(2px, 3px)" }}
          aria-hidden="true"
        />
        <LucideIconComponent
          className={className}
          size={iconSize}
          style={style}
        />
      </span>
    );
  }

  return (
    <LucideIconComponent
      className={className}
      size={iconSize}
      style={style}
    />
  );
}
